    private void Loadtable() {
        try {
            
            DefaultTableModel dtm =(DefaultTableModel) jTable2.getModel();
            dtm.setRowCount(0);
            Session sess = PoolManager.getSessionFactory().openSession();
            Criteria cr = sess.createCriteria(SecurityQuestion.class);
            SecurityQuestion uniqueResult = (SecurityQuestion) cr.uniqueResult();
            
            if (uniqueResult!=null) {
                Vector v = new Vector();
                v.add(uniqueResult.getQ1());
                v.add(uniqueResult.getQ2());
                v.add(uniqueResult.getQ3());
                v.add(uniqueResult.getIdSecurityQuestion());
                
                dtm.addRow(v);
                
                jButton1.setEnabled(false);
                
                
                
                
                
                
                
                
            }else{
            
            jButton1.setEnabled(true);
                
            
            
            }
            sess.close();
            
        } catch (Exception e) {
       e.printStackTrace();
        
        
        }
    
    }
