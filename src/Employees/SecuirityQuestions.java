    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      
   
        
        
        
        if (jButton1.getText().equals("Save")) {
                 try {
            Session sess = PoolManager.getSessionFactory().openSession();
            Transaction tr = sess.beginTransaction();
            SecurityQuestion securityQuestion = new ORM.SecurityQuestion();
            
            securityQuestion.setQ1(Q1.getText());
            
            securityQuestion.setQ2(Q2.getText());
            
            securityQuestion.setQ3(Q3.getText());
            sess.save(securityQuestion);
            tr.commit();
            
            Q1.setText(null);
            Q2.setText(null);
            Q3.setText(null);
            //jButton1.setEnabled(false);
            Loadtable();
            
            sess.close();
            
           // securityQuestion.setQ1(Q1.getText());
            
            
            
        } catch (Exception e) {
       
        e.printStackTrace();
        
        
        }
       
        
        }else{
        
        
            try {
                Session sess = PoolManager.getSessionFactory().openSession();
                Criteria cr = sess.createCriteria(SecurityQuestion.class);
                Transaction 
                    tr    = sess.beginTransaction();
                cr.add(Restrictions.eq("idSecurityQuestion", id));
                SecurityQuestion uniqueResult = (SecurityQuestion) cr.uniqueResult();
                if (uniqueResult!=null) {
                    
                    uniqueResult.setQ1(Q1.getText());
                    uniqueResult.setQ2(Q2.getText());
                    uniqueResult.setQ3(Q3.getText());
                    sess.update(uniqueResult);
                    tr.commit();
                    Loadtable();
                }
                
                
                sess.close();
            } catch (Exception e) {
                
                
                e.printStackTrace();
                
            }
        
        
        
        
        
        }
        
        
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        id=Integer.parseInt(jTable2.getValueAt(jTable2.getSelectedRow(), 3).toString());
        
        try {
            Session sess = PoolManager.getSessionFactory().openSession();
            Criteria cr = sess.createCriteria(SecurityQuestion.class);
            cr.add(Restrictions.eq("idSecurityQuestion", jTable2.getValueAt(jTable2.getSelectedRow(), 3)));
            SecurityQuestion uniqueResult = (SecurityQuestion) cr.uniqueResult();
            
            if (uniqueResult!=null) {
                
                Q1.setText(uniqueResult.getQ1());
                Q2.setText(uniqueResult.getQ2());
                Q3.setText(uniqueResult.getQ3());
                jButton1.setEnabled(true);
                jButton1.setText("Update");
                
                
                
            }
            
            
        sess.close();
        
        } catch (Exception e) {
        
        e.printStackTrace();
        
        }
        
        
        
        
        
        
        
        
        
        
    }//GEN-LAST:event_jTable2MouseClicked

