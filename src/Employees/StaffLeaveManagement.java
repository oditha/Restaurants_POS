    private void save1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save1ActionPerformed
        
        try {
            
            if (!(jComboBox1.getSelectedIndex() == 0 || Fromdate.getText().isEmpty() || Fromdate1.getText().isEmpty())) {
                
                Session openSession = PoolManager.getSessionFactory().openSession();
                Transaction beginTransaction = openSession.beginTransaction();
                
                LeaveManagement leaveManagement = new LeaveManagement();
                
                leaveManagement.setApproveBy(jComboBox2.getSelectedItem().toString());
                leaveManagement.setIsDelete("Active");
                leaveManagement.setLeaveEnd(Fromdate1.getText());
                leaveManagement.setLeaveStart(Fromdate.getText());
                leaveManagement.setNoOfDays(Count.getText());
                leaveManagement.setRequestDate(dateForm.currentdate());
                
                Employees employees = new Employees();
                employees.setIdEmployees(emp.get(jComboBox1.getSelectedItem().toString().split("-")[0]));
                
                leaveManagement.setEmployees(employees);
                openSession.save(leaveManagement);
                beginTransaction.commit();
                
                openSession.close();
                
                NotificationPopup.save();
                jComboBox1.setSelectedIndex(0);
                jComboBox2.setSelectedIndex(0);
                Fromdate.setText(null);
                Fromdate1.setText(null);
                jComboBox1.grabFocus();
                
            }
        } catch (Exception e) {
            
            e.printStackTrace();
            
        }

    }//GEN-LAST:event_save1ActionPerformed

