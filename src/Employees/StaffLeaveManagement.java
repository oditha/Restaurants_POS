    private void Fromdate1FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_Fromdate1FocusLost
        
        try {
            SimpleDateFormat formater = new SimpleDateFormat("yyyy/MM/dd");
            
            long d1 = formater.parse(Fromdate1.getText()).getTime();
            long d2 = formater.parse(Fromdate.getText()).getTime();
            
            long name = Math.abs((d1 - d2) / (1000 * 60 * 60 * 24));
            Count.setText("" + name);
            
        } catch (ParseException ex) {
            
            ex.printStackTrace();
            
        }

    }//GEN-LAST:event_Fromdate1FocusLost

    private void jComboBox1PopupMenuWillBecomeInvisible(javax.swing.event.PopupMenuEvent evt) {//GEN-FIRST:event_jComboBox1PopupMenuWillBecomeInvisible
        
        try {
            String name = jComboBox1.getSelectedItem().toString().split("-")[0];
            Session openSession = PoolManager.getSessionFactory().openSession();
            
            Criteria createCriteria = openSession.createCriteria(LeaveManagement.class);
            createCriteria.createAlias("employees", "e");
            createCriteria.add(Restrictions.eq("e.idEmployees", emp.get(name)));
            createCriteria.add(Restrictions.like("leaveStart", dateForm.currentmonth()+"%"));
            List<LeaveManagement> list = createCriteria.list();
            
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            for (LeaveManagement leaveManagement : list) {
                
                dtm.addRow(new Object[]{
                    leaveManagement.getEmployees().getEmployeName(),
                    leaveManagement.getLeaveStart(),
                    leaveManagement.getNoOfDays(),
                    leaveManagement.getApproveBy()
                
                });
                
            }
            openSession.close();
            
        } catch (Exception e) {
            
            e.printStackTrace();
            
        }
        
    }//GEN-LAST:event_jComboBox1PopupMenuWillBecomeInvisible

