    private void btnReturnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReturnActionPerformed
                
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        try {
            
            System.out.println("Start Search");
            
             Session sess = PoolManager.getSessionFactory().openSession();
            Criteria cr = sess.createCriteria(InvoiceCancel.class);
            cr.add(Restrictions.eq("isDelete", "Active"));
            cr.add(Restrictions.between("cancelDate", TxtStartDatefro.getText(), TxtStartDateTo.getText()));
            cr.addOrder(Order.desc("cancelDate"));
            System.out.println("Searching");
            List<InvoiceCancel> list = cr.list();
            for (InvoiceCancel cr0 : list) {
                System.out.println("Filling Data");
                Vector v = new Vector();
                v.add(cr0.getInvoice().getInvoiceNo());
                v.add(cr0.getCancelDate());
                v.add(cr0.getCancelTime());
                v.add(cr0.getReson());
                v.add(cr0.getCreatedBy());
                model.addRow(v);
                
                System.out.println("Add to table");
                
            }
            
            System.out.println("Done");
            sess.close();
        } catch (Exception e) {
        
        e.printStackTrace();
        
        }
    }//GEN-LAST:event_btnReturnActionPerformed

