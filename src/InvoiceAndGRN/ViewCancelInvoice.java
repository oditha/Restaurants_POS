    private void tableload() {
        
        DefaultTableModel mode2 = (DefaultTableModel) jTable1.getModel();
        
        try {
            Session sess = PoolManager.getSessionFactory().openSession();
            Criteria cr = sess.createCriteria(InvoiceCancel.class);
            cr.add(Restrictions.eq("isDelete", "Active"));
            cr.addOrder(Order.desc("cancelDate"));
           
            List<InvoiceCancel> list = cr.list();
            for (InvoiceCancel cr0 : list) {
                Vector v = new Vector();
                v.add(cr0.getInvoice().getInvoiceNo());
                v.add(cr0.getCancelDate());
                v.add(cr0.getCancelTime());
                v.add(cr0.getReson());
                v.add(cr0.getCreatedBy());
               
                mode2.addRow(v);
                
                
                
            }
            sess.close();
        } catch (Exception e) {
        
        e.printStackTrace();
        
        }
        
        
    }
