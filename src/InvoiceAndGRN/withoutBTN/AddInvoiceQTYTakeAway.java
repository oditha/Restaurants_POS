    private void SetInvoiceTotal() {
       
        Double Total = 0.0;
        
        for (int i = 0; i < newInvoiceArsh.jTableTW.getRowCount(); i++) {
            
            Total += Double.parseDouble(newInvoiceArsh.jTableTW.getValueAt(i, 4).toString());
            
        }
        
        newInvoiceArsh.txtTWsetTotal.setText(Decimal_Formats.Price(Total));
        
    }
