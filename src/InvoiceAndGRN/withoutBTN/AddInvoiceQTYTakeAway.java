    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        boolean flag = true;

        int tblQty = 0;
        double tblAmount = 0.0;
        int qty = Integer.parseInt(jTextField1.getText());

//        openSession.close();

        for (int i = 0; i < newInvoiceArsh.jTableTW.getRowCount(); i++) {

            String itemCodeTBL = newInvoiceArsh.jTableTW.getValueAt(i, 5).toString();

            if (itemCodeTBL.equals(ItemCode)) {

                flag = false;

                tblQty = Integer.parseInt(newInvoiceArsh.jTableTW.getValueAt(i, 3).toString());
                newInvoiceArsh.jTableTW.setValueAt((tblQty + qty), i, 3);
                newInvoiceArsh.jTableTW.setValueAt(((tblQty + qty) * newInvoiceArsh.ItemDataTW.get(ItemCode).getPrice()), i, 4);

                break;

            } else {

                flag = true;

            }
        }

        System.out.println(flag);
        
        if (flag == true) {
            DefaultTableModel dtm = (DefaultTableModel) newInvoiceArsh.jTableTW.getModel();
         
//        int qty = Integer.parseInt(jTextField1.getText());
            dtm.addRow(new Object[]{
                newInvoiceArsh.ItemDataTW.get(ItemCode).getIdItem(),
                newInvoiceArsh.ItemDataTW.get(ItemCode).getItemName(),
                Decimal_Formats.Price(newInvoiceArsh.ItemDataTW.get(ItemCode).getPrice()),
                qty,
                Decimal_Formats.Price(newInvoiceArsh.ItemDataTW.get(ItemCode).getPrice() * qty),
                newInvoiceArsh.ItemDataTW.get(ItemCode).getCode(),
                newInvoiceArsh.ItemDataTW.get(ItemCode).getIsStock()
            
            });

        }

        SetInvoiceTotal();
        
        this.dispose();


    }//GEN-LAST:event_jButton3ActionPerformed

